           <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;冰雹是春夏季节一种对农业生产危害较大的灾害性天气。</p> 
          <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;冰雹出现时，常常伴有大风、剧烈的降温和强雷电现象。一场冰雹袭击，轻者减产，重者绝收。气象台站根据天气图、卫星云图分析和雷达监测，虽能提前作出预报，但准确中仍然不是很理想。经过长期的观察实践，人们总结了一些常见的预测冰雹的经验。</p>
<p>感冷热</p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如果下雹季节的早晨凉，湿度大，中午太阳辐射强烈，造成空气对流旺盛，则易发展成积雨云而形成冰雹。故有“早晨凉飕飕，午后打破头”、“早晨露水重，后晌冰雹猛”的说法。</p>
<p>辨风向</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;下雹前常常出现大风而风向变化则烈。农谚有“恶云见风长，冰雹随风落”、“风拧云转、雹子片”等说法。另外如果连续刮南风以后，风向转为西北或北风，风力加大时，则冰雹往往伴随而来因此有“不刮东风不下雨，不刮南风不降雹”之说。</p>
